CREATE TABLE ERI(
	incident	VARCHAR(100),
	borough		VARCHAR(100),
	creation_date	TIMESTAMP,
	closed_date	TIMESTAMP,
	latitude	NUMERIC,
	longitude	NUMERIC,
	PRIMARY KEY (incident, creation_date, latitude, longitude)
);
