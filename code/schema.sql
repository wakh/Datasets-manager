	closed_date	TIMESTAMP,
	latitude	NUMERIC,
	longitude	NUMERIC,
	PRIMARY KEY (complaint_type, creation_date, latitude, longitude)
);
GRANT ALL PRIVILEGES ON ERI, ServiceRequests TO dbms_project_user;
