	x28 VARCHAR(1000), x29 VARCHAR(1000), x30 VARCHAR(1000), x31 VARCHAR(1000), x32 VARCHAR(1000), x33 VARCHAR(1000), x34 VARCHAR(1000), x35 VARCHAR(1000), x36 VARCHAR(1000),
	x37 VARCHAR(1000), x38 VARCHAR(50), x39 VARCHAR(50), x40 VARCHAR(50));
CREATE TABLE ServiceRequests(
	complaint_type	VARCHAR(100),
	borough		VARCHAR(100),
